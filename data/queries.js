async function getAllDevicesFromDB() {
  const queryText = `
    SELECT * FROM devices;
  `;
  try {
    const result = await query(queryText);
    return result.rows;
  } catch (error) {
    console.error("Error fetching all devices data:", error);
    throw error;
  }
}
